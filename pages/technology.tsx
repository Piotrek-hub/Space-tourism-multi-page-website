import Head from 'next/head';
import { useState } from 'react';
import Navbar from '../components/Shared/Navbar';
import TechnologyComponent from '../components/Technology';
import { ITechnology } from '../types/interfaces';

enum technologyMenuChoice {
	LAUNCH_VEHICLE = 'LAUNCH VEHICLE',
	SPACEPORT = 'SPACEPORT',
	SPACE_CAPSULE = 'SPACE CAPSULE',
}

export default function Technology() {
	const [technologyData, setTechnologyData] = useState<ITechnology>(
		technologyDataArray[2]
	);

	const [menuChoice, setMenuChoice] = useState<technologyMenuChoice>(
		technologyMenuChoice.LAUNCH_VEHICLE
	);

	const setTechnologyDataChoice = (choice: technologyMenuChoice) => {
		setTechnologyData(
			technologyDataArray.filter((data: ITechnology) =>
				data.title.includes(choice)
			)[0]
		);
		setMenuChoice(choice);
	};

	return (
		<>
			<Head>
				<title>Technology | Space Tourism</title>
				<meta
					name="description"
					content="Generated by create next app"
				/>
				<link rel="icon" href="/favicon.ico" />
			</Head>

			<div className="w-screen grid h-screen mobile:min-h-screen place-items-start content-start mobile:place-items-center bg-technology-desktop tablet:bg-technology-tablet mobile:bg-technology-mobile bg-no-repeat bg-cover select-none bg-fixed mobile:h-auto ">
				<Navbar />
				<div className="font-barlowCondensed self-start pl-[200px] text-[28px] my-[50px] tablet:text-[20px] mobile:my-[20px] tracking-[4.72px] tablet:px-[50px] mobile:text-[16px] mobile:self-center">
					<span className="text-white/[0.5] font-bold pr-5">03</span>
					<span className="text-white">SPACE LAUNCH 101</span>
				</div>
				<div className="grid self-start h-auto overflow-hidden tablet:place-items-center">
					<TechnologyComponent
						{...technologyData}
						setTechnologyDataChoice={setTechnologyDataChoice}
						technologyChoice={menuChoice}
					/>
				</div>
			</div>
		</>
	);
}

